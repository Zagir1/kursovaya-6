public class CreatePDF {
	
	/** The times. */
	private BaseFont times = null;

    /**
     * Creates the.
     *
     * @throws IOException Signals that an I/O exception has occurred.
     */
    public void Create() throws IOException {
      	
    	Document document = new Document();
    	
    	String filepath = new File("").getCanonicalPath();
		String[] parsfilepath = filepath.split("/");
		
		int lengthpath = parsfilepath.length;
		String abspath=""; 
		for(int i=0;i<(lengthpath-1);i++) {
			abspath=abspath+parsfilepath[i]+"/";
		}
		filepath=abspath+"webapps/kursovaya/Check.pdf";
		String fontpath =abspath+"webapps/kursovaya/fonts/times.ttf";
    	
		try {	
			PdfWriter.getInstance(document, new FileOutputStream(filepath));
		} catch (FileNotFoundException | DocumentException e) {
			e.printStackTrace();
		}
					
		document.open(); 
		
		//BaseFont times = null;
		try {
			times = BaseFont.createFont(fontpath, "cp1251", BaseFont.EMBEDDED);
		} catch (DocumentException | IOException e) {
			e.printStackTrace();
		}
		try {
			document.add(new Paragraph("ООО ФастКлин", new Font(times,14)));
		} catch (DocumentException e1) {
			e1.printStackTrace();
		}

	    document.close(); 
    }
}
